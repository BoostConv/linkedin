// ─── Generate From Idea Dialog ───────────────────────────────
function GenerateFromIdeaDialog({
  idea,
  open,
  onOpenChange,
  onGenerated,
}: {
  idea: Idea;
  open: boolean;
  onOpenChange: (open: boolean) => void;
  onGenerated: () => void;
}) {
  const [generating, setGenerating] = useState(false);
  const [result, setResult] = useState<AutoGeneratedPost | null>(null);
  const [editedContent, setEditedContent] = useState("");
  const [error, setError] = useState<string | null>(null);
  const [revalidation, setRevalidation] = useState<ValidationResult | null>(null);

  const handleGenerate = async () => {
    setGenerating(true);
    setResult(null);
    setError(null);
    setRevalidation(null);
    try {
      const generated = await api.generateAutoPost({
        topic: idea.raw_input,
        source_url: idea.source_url || undefined,
        idea_id: idea.id,
        save_as_draft: true,
      });
      setResult(generated);
      setEditedContent(generated.content);
    } catch (err) {
      setError(err instanceof Error ? err.message : "Erreur de génération");
    } finally {
      setGenerating(false);
    }
  };

  const handleRevalidate = async () => {
    if (!editedContent) return;
    const validation = await api.validatePost(editedContent);
    setRevalidation(validation);
  };

  const handleSaveEdit = async () => {
    if (!result?.post_id || !editedContent) return;
    await api.updatePost(result.post_id, { content: editedContent });
  };

  useEffect(() => {
    if (open && !result && !generating) handleGenerate();
  }, [open]);

  const validation = revalidation || result?.validation;

  return (
    <Dialog open={open} onOpenChange={(v) => { onOpenChange(v); if (!v && result) onGenerated(); }}>
      <DialogContent className="max-w-[680px] w-full max-h-[95vh] overflow-y-auto">
        <DialogHeader>
          <DialogTitle>Générer un post depuis cette idée</DialogTitle>
        </DialogHeader>
        <div className="space-y-4 pt-2">
          <div className="bg-gray-50 rounded-lg px-4 py-3">
            <p className="text-xs font-medium text-gray-500 mb-1">Idée originale</p>
            <p className="text-sm text-gray-800 whitespace-pre-wrap">{idea.raw_input}</p>
            {idea.source_url && (
              <a href={idea.source_url} target="_blank" rel="noopener noreferrer"
                className="text-xs text-blue-500 hover:underline mt-1 block">{idea.source_url}</a>
            )}
          </div>
          {generating && (
            <div className="flex items-center justify-center py-8 gap-3">
              <Spinner />
              <p className="text-sm text-gray-600">L&apos;IA analyse, choisit le format et génère le post...</p>
            </div>
          )}
          {error && (
            <div className="bg-red-50 text-red-700 rounded px-4 py-3 text-sm">
              {error}
              <Button variant="outline" size="sm" className="mt-2" onClick={handleGenerate}>Réessayer</Button>
            </div>
          )}
          {result && (
            <div className="space-y-4">
              <div className="bg-blue-50 rounded-lg px-4 py-3 space-y-1">
                <div className="flex items-center gap-2 flex-wrap">
                  <Badge className="bg-blue-100 text-blue-700">{result.pillar_name}</Badge>
                  <Badge className="bg-indigo-100 text-indigo-700">{result.template_name}</Badge>
                  {validation && (
                    <Badge className={validation.score >= 80 ? "bg-green-100 text-green-700" : validation.score >= 50 ? "bg-yellow-100 text-yellow-700" : "bg-red-100 text-red-700"}>
                      Anti-IA: {validation.score}/100
                    </Badge>
                  )}
                  {result.post_id && <Badge variant="outline">Sauvé en brouillon</Badge>}
                </div>
                <p className="text-xs text-blue-600 mt-1">{result.reasoning}</p>
              </div>
              <Textarea value={editedContent} onChange={(e) => setEditedContent(e.target.value)}
                className="min-h-[400px] text-[14px] leading-[1.42] text-gray-900 border-gray-200"
                style={{ fontFamily: '-apple-system, BlinkMacSystemFont, "Segoe UI", Roboto, "Helvetica Neue", Arial, sans-serif' }}
              />
              <div className="flex gap-2 flex-wrap">
                <Button variant="outline" onClick={handleRevalidate} size="sm">Re-valider anti-IA</Button>
                <Button variant="outline" onClick={handleSaveEdit} size="sm" disabled={editedContent === result.content}>Sauvegarder</Button>
                <Button onClick={handleGenerate} variant="outline" size="sm" disabled={generating}>Regénérer</Button>
              </div>
              {validation && validation.issues.length > 0 && (
                <div className="space-y-2">
                  {validation.issues.map((issue, i) => (
                    <div key={i} className={`text-sm rounded px-3 py-2 ${issue.severity === "error" ? "bg-red-50 text-red-700" : "bg-yellow-50 text-yellow-700"}`}>
                      <span className="font-medium">{issue.rule}</span>
                      <span className="block text-xs mt-0.5">{issue.detail}</span>
                    </div>
                  ))}
                </div>
              )}
            </div>
          )}
        </div>
      </DialogContent>
    </Dialog>
  );
}

