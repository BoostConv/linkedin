// ─── Idea Card ───────────────────────────────────────────────
function IdeaCard({
  idea,
  pillarName,
  onDelete,
  onArchive,
  onGenerate,
  onChangePriority,
}: {
  idea: Idea;
  pillarName: string;
  onDelete: (id: string) => void;
  onArchive: (id: string) => void;
  onGenerate: (idea: Idea) => void;
  onChangePriority: (id: string, priority: string) => void;
}) {
  const priority = priorityConfig[idea.priority] || priorityConfig.medium;
  const status = statusConfig[idea.status] || statusConfig.new;
  const title = idea.raw_input.split("\n")[0].substring(0, 80);
  const body = idea.raw_input.split("\n").slice(1).join("\n").trim();

  return (
    <Card className="hover:shadow-md transition-shadow h-full flex flex-col">
      <CardContent className="p-4 flex flex-col flex-1">
        <div className="flex items-center gap-1.5 mb-2 flex-wrap">
          <Badge className={`${status.className} text-[10px] px-1.5 py-0`}>{status.label}</Badge>
          <button onClick={() => {
            const priorities = ["high", "medium", "low"];
            const next = priorities[(priorities.indexOf(idea.priority) + 1) % 3];
            onChangePriority(idea.id, next);
          }}>
            <Badge className={`${priority.className} cursor-pointer hover:opacity-80 text-[10px] px-1.5 py-0`}>
              {priority.label}
            </Badge>
          </button>
          {pillarName && <Badge className="bg-blue-50 text-blue-600 text-[10px] px-1.5 py-0">{pillarName}</Badge>}
        </div>

        <p className="text-sm font-semibold text-gray-900 line-clamp-2">{title}</p>
        {body && <p className="text-xs text-gray-600 mt-1 line-clamp-3">{body}</p>}

        {idea.source_url && (
          <a href={idea.source_url} target="_blank" rel="noopener noreferrer"
            className="text-[10px] text-blue-500 hover:underline mt-1 block truncate">{idea.source_url}</a>
        )}

        {idea.suggested_angle && (
          <div className="mt-2 bg-blue-50 rounded px-2 py-1.5">
            <p className="text-[10px] font-medium text-blue-700">Angle suggéré</p>
            <p className="text-[10px] text-blue-600 line-clamp-2">{idea.suggested_angle}</p>
          </div>
        )}

        <div className="mt-auto pt-3 border-t flex items-center justify-between gap-1">
          <Button variant="default" size="sm" onClick={() => onGenerate(idea)} className="h-7 text-xs px-2">Rédiger</Button>
          <div className="flex gap-1">
            <Button variant="ghost" size="sm" onClick={() => onArchive(idea.id)} className="h-7 text-xs px-2 text-gray-500">Archiver</Button>
            <Button variant="ghost" size="sm" className="h-7 text-xs px-2 text-red-500 hover:text-red-700" onClick={() => onDelete(idea.id)}>✕</Button>
          </div>
        </div>
      </CardContent>
    </Card>
  );
}

